let score = 75; // この値を変更してテストしてください

// 以下に条件分岐(if文)を記述してください
// 80点以上: "優"
// 70点以上 80点未満: "良"
// 60点以上 70点未満: "可"
// 60点未満: "不可"

if (score >= 80) {
  console.log("優");
} else if (score >= 70) {
  console.log("良");
} else if (score >= 60) {
  console.log("可");
} else {
  console.log("不可");
}
